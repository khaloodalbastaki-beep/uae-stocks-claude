def _market_status(now: datetime) -> dict:
    # UAE markets trade Mon–Fri. (Demo logic; a real circular feed would override.)
    weekday = now.weekday()  # Mon=0 .. Sun=6
    is_weekend = weekday >= 5
    return {
        "state": "closed" if is_weekend else "open",
        "label_en": "Markets closed (weekend)" if is_weekend else "Markets open",
        "label_ar": "السوق مغلق (عطلة)" if is_weekend else "السوق مفتوح",
        "note_en": "Showing latest available data.",
        "circulars": [],
        "as_of": _iso(now),
    }


