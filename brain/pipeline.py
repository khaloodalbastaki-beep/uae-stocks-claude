"""
The ingest → score → narrate → emit pipeline. This is the Mac-mini "brain" the launchd
job runs. It mirrors the agentec/finance pattern: deterministic Python computes
everything, then writes plain JSON that the static PWA reads (GitHub-as-DB).

End-to-end for each security (the blueprint's required "one end to end flow"):
  ingest quote + fundamentals + disclosures + meetings + dividends + ownership
  → score (deterministic Growth/Stability/Dividend)
  → enrich disclosures + run AI analysis (narration only; numbers stay deterministic)
  → emit data/stocks/<SYMBOL>.json + a compact card into data/universe.json

Plus market-wide files: indices, events strip, commodities, global events, and meta
(disclaimers, market status, source registry).
"""
